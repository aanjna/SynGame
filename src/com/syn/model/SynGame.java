package com.syn.model;

public class SynGame {

	public String title;
	public String platform;
	public String score;
	public String genre;	
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getPlatform() {
		return platform;
	}
	public void setPlatform(String platform) {
		this.platform = platform;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	public String getEditors_choice() {
		return editors_choice;
	}
	public void setEditors_choice(String editors_choice) {
		this.editors_choice = editors_choice;
	}
	public String editors_choice;
}
